/*

DDL - Data Definition Language | Linguagem de Definição de Dados
São os comandos que interagem com os objetos do banco.
CREATE, ALTER e DROP (excluir).

DML - Data Manipulation Language | Linguagem de Manipulação de Dados
São os comandos que interagem com os dados dentro das tabelas.
INSERT, DELETE e UPDATE

DQL - Data Query Language | Linguagem de Consulta de dados
São os comandos de consulta.
SELECT.
Em alguns livros o SELECT fica na DML em outros tem esse grupo próprio.

DTL/TCL - Data Transaction Language | Linguagem de Transação de Dados
São os comandos para controle de transação.
START TRANSACTION ou BEGIN (iniciar), COMMIT (confirmar), SAVEPOINT (ponto de restauração) e ROLLBACK (cancelar).

DCL - Data Control Language | Linguagem de Controle de Dados
São os comandos para controlar a parte de segurança do banco de dados.
GRANT (conceder), REVOKE (revogar) e DENY (negar).

*/